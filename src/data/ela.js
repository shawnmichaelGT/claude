export function getQuestions(topic, count = 10) {
  const bank = topic === 'mixed' ? QUESTIONS : QUESTIONS.filter(q => q.topic === topic)
  const shuffled = shuffleArray(bank)
  const result = []
  while (result.length < count) result.push(...shuffled)
  return result.slice(0, count)
}
